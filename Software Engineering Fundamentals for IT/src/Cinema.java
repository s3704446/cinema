
public class Cinema {
	
	private String cinemaID;
	private String cinemaName;
	
	public Cinema(String cinemaID, String cinemaName) {
		this.cinemaID = cinemaID;
		this.cinemaName = cinemaName;
	}
	
	
	public String getCinemaID() {
		return cinemaID;
	}
	
	public String getCinemaName() {
		return cinemaName;
	}

}
